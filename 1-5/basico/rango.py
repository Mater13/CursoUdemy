# Ejercicio1: iterar un rango de 0 a 10 e imprimir numeros divisibles entre 3
print('Ejercicio 1, Numeros divisibles entre 3')
for i in range(11):
    if i % 3 == 0:
        print(i)

# Ejercicio2: Crear un rango de numero de 2 a 6, e imprimelos
print('Ejercicio 2, rango de 2 a 6')
for i in range(2, 7):
    print(i)


# Ejercicio3: crear un rango de 3 a 10,
# pero con incremento de 2 en 2, en lugar de 1 en 1
