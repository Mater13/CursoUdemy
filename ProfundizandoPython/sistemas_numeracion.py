# profundizando Sistemas Numeracion
# Decimal
a = 10
print(f'Valor en Decimal (10) >> a: {a}')
# Binario 
a = 0b1010
print(f'Valor en Binario (0b1010) >> a: {a}')
# Octal
a = 0o12
print(f'Valor en Ocatal (0o12) >> a: {a}')
# Hexadecimal
a = 0xA
print(f'Valor en Hexadecimal (0xA) >> a: {a}')


# Convertir un tipo entero, incluyendo la base
# Base Decimal
a = int('23', 10)
print(f'Valor en Decimal >> a: {a}')
# Base Binario
a = int('10111', 2)
print(f'Valor decimal, equivalente (10111) en binario >> a: {a}')
# Base Octal
a = int('27', 8)
print(f'Valor decimal, equivalente (27) en Octal a: >> {a}')
# Base Hexadecimal
a = int('17', 16)
print(f'Valor decimal, equivalente (17) en Hexadecimal >> a: {a}')
